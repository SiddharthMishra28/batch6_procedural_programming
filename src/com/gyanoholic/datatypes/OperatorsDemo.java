package com.gyanoholic.datatypes;

public class OperatorsDemo {
	
	// Logical - AND (&&) OR (||)
	// Mathematical - +, -, *, /, %
	// Comparison - ==, >,  <
	// Ternary - 
	// Shift Operators - 
	
	public static void main(String[] args) {
		System.out.println(1>5);
		System.out.println(2>1 && 3>5);
		System.out.println((5%2 > 1) && (2-10 > 3));
		System.out.println((5%2 > 1) || (10-2 > 3));
	}
}
